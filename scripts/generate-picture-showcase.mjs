import { readdirSync, statSync, writeFileSync } from "node:fs";
import { extname, join } from "node:path";

const projectDir = "public/projects/2026";
const showcaseDir = "public/projects/2026/showcase";
const outputFile = "src/data/pictureShowcase.generated.js";
const imageExtensions = new Set([".jpg", ".jpeg", ".png", ".webp"]);

const categoryRules = [
  {
    category: "team",
    label: "Team photo",
    match: (filename) =>
      filename.startsWith("team") ||
      filename.startsWith("teams") ||
      filename.includes("place") ||
      filename.includes("winner"),
  },
  {
    category: "presentations",
    label: "Project photo",
    match: (filename, parentDir) =>
      parentDir === projectDir &&
      !filename.includes("place") &&
      !filename.includes("winner"),
  },
  {
    category: "presentations",
    label: "Presentation",
    match: (filename) => filename.startsWith("presentation"),
  },
  {
    category: "workshops",
    label: "Workshop",
    match: (filename) => filename.startsWith("workshop"),
  },
];

function getPictureMeta(filename, parentDir) {
  const normalized = filename.toLowerCase();
  const rule = categoryRules.find((categoryRule) =>
    categoryRule.match(normalized, parentDir)
  );

  if (rule) {
    return {
      category: rule.category,
      label: rule.label,
    };
  }

  return {
    category: "all",
    label: "NSH 2026 photo",
  };
}

function getImagesFromDir(dir) {
  return readdirSync(dir)
    .filter((filename) => {
      const path = join(dir, filename);
      return (
        statSync(path).isFile() &&
        imageExtensions.has(extname(filename).toLowerCase())
      );
    })
    .sort((a, b) => a.localeCompare(b, undefined, { numeric: true }))
    .map((filename) => {
      const meta = getPictureMeta(filename, dir);

      return {
        src: `/${join(dir.replace("public/", ""), filename)}`,
        label: meta.label,
        category: meta.category,
      };
    });
}

const pictures = [
  ...getImagesFromDir(projectDir),
  ...getImagesFromDir(showcaseDir),
];

const output = `// This file is generated by scripts/generate-picture-showcase.mjs.
// Add images to public/projects/2026 or public/projects/2026/showcase, then run npm run generate:pictures.

export const pictureShowcase = ${JSON.stringify(pictures, null, 2)};
`;

writeFileSync(outputFile, output);

console.log(`Generated ${outputFile} with ${pictures.length} pictures.`);
